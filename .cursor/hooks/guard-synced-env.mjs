#!/usr/bin/env node
/**
 * preToolUse + beforeShellExecution — block direct edits to pnpm env:sync outputs.
 * Source of truth: .env.config + .env.secret → run `pnpm env:sync`.
 */
import { emit, log, parseStdinJson, resolveRepoRoot } from "./_hook-utils.mjs";

const TAG = "guard-synced-env";

/** Keep in sync with scripts/sync-env.mjs explicit destinations. */
const SYNCED_ENV_RELATIVE = [
  ".env",
  ".env.local",
  "apps/app/.env.local",
  "apps/api/.env.local",
  "apps/web/.env.local",
  "packages/database/.env",
  "packages/cms/.env.local",
  "packages/internationalization/.env.local",
];

const EDITABLE_ENV_RELATIVE = new Set([".env.config", ".env.secret"]);

const EDIT_TOOLS = new Set([
  "Write",
  "StrReplace",
  "Delete",
  "EditNotebook",
  "ApplyPatch",
]);

const DENY = {
  permission: "deny",
  user_message:
    "This env file is generated by pnpm env:sync. Edit .env.config or .env.secret, then run pnpm env:sync.",
  agent_message:
    "Blocked direct edit of synced env output. Edit .env.config (config/store IDs) or .env.secret (tokens/secrets), then run pnpm env:sync. Hook: guard-synced-env.mjs.",
};

const SHELL_WRITE_HINT =
  /(?:>>?|\|\s*tee\b|\bSet-Content\b|\bAdd-Content\b|\bOut-File\b|\bsp\s)/i;

function normalizePath(rawPath, repoRoot) {
  if (typeof rawPath !== "string" || rawPath.length === 0) {
    return "";
  }

  let normalized = rawPath.replace(/\\/g, "/");

  if (repoRoot) {
    const root = repoRoot.replace(/\\/g, "/").replace(/\/$/, "");
    if (normalized.startsWith(root)) {
      normalized = normalized.slice(root.length).replace(/^\//, "");
    }
  }

  return normalized.replace(/^\.\//, "");
}

function isSyncedEnvRelativePath(relativePath) {
  if (!relativePath) {
    return false;
  }

  if (EDITABLE_ENV_RELATIVE.has(relativePath)) {
    return false;
  }

  if (relativePath.endsWith(".env.example")) {
    return false;
  }

  return SYNCED_ENV_RELATIVE.includes(relativePath);
}

function extractPath(input) {
  const toolInput = input.tool_input ?? input.arguments ?? input.input ?? {};

  if (typeof toolInput === "string") {
    return toolInput;
  }

  const candidates = [
    toolInput.path,
    toolInput.file_path,
    toolInput.target_notebook,
    toolInput.notebook_path,
    input.path,
    input.file_path,
  ];

  for (const value of candidates) {
    if (typeof value === "string" && value.length > 0) {
      return value;
    }
  }

  return "";
}

function isAllowedShellCommand(command) {
  const normalized = command.trim().replace(/\s+/g, " ");

  if (/pnpm\s+(env:sync|env:doctor)\b/.test(normalized)) {
    return true;
  }

  if (/sync-env\.mjs\b/.test(normalized)) {
    return true;
  }

  return false;
}

function isBlockedShellEnvWrite(command, repoRoot) {
  if (!command || isAllowedShellCommand(command)) {
    return false;
  }

  if (!SHELL_WRITE_HINT.test(command)) {
    return false;
  }

  for (const relativePath of SYNCED_ENV_RELATIVE) {
    const pathPattern = relativePath.replace(/\./g, "\\.").replace(/\//g, "[/\\\\]");
    if (new RegExp(pathPattern, "i").test(command)) {
      return true;
    }
  }

  // Catch absolute paths that include synced env segments
  for (const relativePath of SYNCED_ENV_RELATIVE) {
    const normalized = normalizePath(relativePath, repoRoot);
    if (command.includes(normalized)) {
      return true;
    }
  }

  return false;
}

const input = parseStdinJson();

if (input === null) {
  log(TAG, "invalid stdin JSON; allowing");
  emit({ permission: "allow" });
  process.exit(0);
}

const repoRoot = resolveRepoRoot();
const command = typeof input.command === "string" ? input.command : "";
const toolName = input.tool_name ?? input.toolName ?? "";

if (command && isBlockedShellEnvWrite(command, repoRoot)) {
  log(TAG, `blocked shell write: ${command.slice(0, 120)}`);
  emit(DENY);
  process.exit(0);
}

if (toolName && !EDIT_TOOLS.has(toolName)) {
  emit({ permission: "allow" });
  process.exit(0);
}

const relativePath = normalizePath(extractPath(input), repoRoot);

if (isSyncedEnvRelativePath(relativePath)) {
  log(TAG, `blocked edit: ${relativePath}`);
  emit(DENY);
  process.exit(0);
}

emit({ permission: "allow" });
process.exit(0);

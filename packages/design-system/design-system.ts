/**
 * Public craft surface for apps and domain packages (`@repo/auth`, `apps/*`).
 *
 * Import from `@repo/design-system/design-system` — not from internal paths
 * like `components/afenda-ui/recipes` or `components/blocks/block-recipes`.
 *
 * | Layer | API | Use in |
 * |-------|-----|--------|
 * | Utility | `cn` | className composition |
 * | Primitive recipe | `recipe`, `afendaRecipe` | forms, auth, app wiring |
 * | Block recipe | `blockRecipe`, `afendaBlockRecipe` | ERP blocks + block stories only |
 * | Primitives | `Button`, `Field`, … | all UI controls |
 * | Blocks | `FormSection`, `PageHeader`, … | admin / ERP surfaces |
 */

/** Root provider — includes afenda-ui `Toaster` (sonner) for toast.promise/toast.* in blocks. */
export const DesignSystemProvider = ({
  children,
  ...properties
}: ThemeProviderProps) =>
  createElement(
    ThemeProvider,
    properties,
    createElement(TooltipProvider, null, children),
    createElement(Toaster)
  );

